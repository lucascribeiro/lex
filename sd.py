def sd(character):
    if(character=='e'):
        return "de"
    if(character=='o'):
        return "do"
    if(character.isalpha()):
        return "Id"
    if(character.isnumeric()):
        return "Id"
    if(character.isspace()):
        return "Id"    
    if(character=='_'):
        return "Id"
    return "bad"

