def se(character):
    if(character=='l'):
        return "el"
    if(character=='n'):
        return "en"
    if(character=='x'):
        return "ex"    
    if(character.isalpha()):
        return "Id"
    if(character.isnumeric()):
        return "Id"
    if(character.isspace()):
        return "Id"    
    if(character=='_'):
        return "Id"
    return "bad"    

