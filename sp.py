def sp(character):
    if(character=='a'):
        return "pa"
    if(character=='r'):
        return "pr"
    if(character=='u'):
        return "pu" 
    if(character.isalpha()):
        return "Id"
    if(character.isnumeric()):
        return "Id"
    if(character.isspace()):
        return "Id"    
    if(character=='_'):
        return "Id"
    return "bad"   

